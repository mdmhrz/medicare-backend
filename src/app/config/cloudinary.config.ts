import { v2 as cloudinary, UploadApiResponse, } from "cloudinary"
import { envVars } from "./env"
import AppError from "../errorHelper/AppError";
import status from "http-status";



cloudinary.config({
    cloud_name: envVars.CLOUDINARY.CLOUDINARY_CLOUD_NAME,
    api_key: envVars.CLOUDINARY.CLOUDINARY_API_KEY,
    api_secret: envVars.CLOUDINARY.CLOUDINARY_API_SECRET
});

//upload file to cloudinary
export const uploadFileToCloudinary = async (
    buffer: Buffer,
    fileName: string,
): Promise<UploadApiResponse> => {

    if (!buffer || !fileName) {
        throw new AppError(status.BAD_REQUEST, "File buffer and file name are required for upload")
    }


    const extension = fileName.split(".").pop()?.toLocaleLowerCase();

    const fileNameWithOutExtension = fileName
        .split(".")
        .slice(0, -1)
        .join(".")
        .toLowerCase()
        .replace(/\s/g, "-")
        .replace(/[^a-z0-9-]/g, "");

    const uniqueName =
        Math.random()
            .toString(36)
            .substring(2)
        + "-"
        + Date.now()
        + "-"
        + fileNameWithOutExtension;

    const folder = extension === "pdf" ? "pdfs" : "images";

    return new Promise((resolve, reject) => {
        cloudinary.uploader.upload_stream({
            resource_type: "auto",
            folder: `Medicare/${folder}`,
            public_id: uniqueName,
        }, (error, result) => {
            if (error) {
                return reject(new AppError(status.INTERNAL_SERVER_ERROR, "Error uploading file to cloudinary"));
            }

            resolve(result as UploadApiResponse);
        }
        ).end(buffer);
    })


}



// delete file from cloudinary
export const deleteFileFromCloudinary = async (url: string) => {
    try {
        const regex = /\/v\d+\/(.+?)(?:\.[a-zA-Z0-9]+)+$/;

        const match = url.match(regex);

        if (match && match[1]) {
            const publicId = match[1];
            await cloudinary.uploader.destroy(
                publicId,
                {
                    resource_type: "image",
                }
            );

            console.log(`Deleted file with public ID: ${publicId}`);
        }
    } catch (error) {
        console.log("Error deleting file form cloudinary", error);
        throw new AppError(status.INTERNAL_SERVER_ERROR, "Error deleting file form cloudinary");
    }
}



